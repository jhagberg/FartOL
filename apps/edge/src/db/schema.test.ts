describe('schema (phase 2): competitors.source column', () => {
  test('source defaults to "walkup" — backfill semantic for pre-2.0 rows', () => {
    const handle = openDatabase(':memory:');
    try {
      // Seed competition + class.
      handle.sqlite
        .prepare(
          "INSERT INTO competitions (id, name, date, receipt_template, auto_print, created_at_ms) VALUES ('c1', 't', '2026-05-14', 'classic', 0, 1)"
        )
        .run();
      handle.sqlite
        .prepare("INSERT INTO classes (id, competition_id, name) VALUES ('cl1', 'c1', 'H21')")
        .run();
      // Insert competitor WITHOUT specifying source — default should fire.
      handle.sqlite
        .prepare(
          "INSERT INTO competitors (id, competition_id, name, class_id) VALUES ('p2-comp1', 'c1', 'Pre20', 'cl1')"
        )
        .run();
      const row = handle.sqlite
        .prepare<unknown[], { source: string }>('SELECT source FROM competitors WHERE id=?')
        .get('p2-comp1');
      assert.ok(row);
      assert.equal(row.source, 'walkup');
    } finally {
      handle.close();
    }
  });

  test('source accepts "entrylist" and "meos" overrides', () => {
    const handle = openDatabase(':memory:');
    try {
      handle.sqlite
        .prepare(
          "INSERT INTO competitions (id, name, date, receipt_template, auto_print, created_at_ms) VALUES ('c1', 't', '2026-05-14', 'classic', 0, 1)"
        )
        .run();
      handle.sqlite
        .prepare("INSERT INTO classes (id, competition_id, name) VALUES ('cl1', 'c1', 'H21')")
        .run();
      handle.sqlite
        .prepare(
          "INSERT INTO competitors (id, competition_id, name, class_id, source) VALUES ('p2-comp2', 'c1', 'EL', 'cl1', 'entrylist')"
        )
        .run();
      handle.sqlite
        .prepare(
          "INSERT INTO competitors (id, competition_id, name, class_id, source) VALUES ('p2-comp3', 'c1', 'M', 'cl1', 'meos')"
        )
        .run();
      const rows = handle.sqlite
        .prepare<
          unknown[],
          { id: string; source: string }
        >('SELECT id, source FROM competitors WHERE id IN (?, ?) ORDER BY id')
        .all('p2-comp2', 'p2-comp3');
      assert.equal(rows.length, 2);
      assert.equal(rows[0]?.source, 'entrylist');
      assert.equal(rows[1]?.source, 'meos');
    } finally {
      handle.close();
    }
  });
});

describe('schema (phase 2): hired_cards compound PK', () => {
  test('same card_number under different competition_id is allowed', () => {
    const handle = openDatabase(':memory:');
    try {
      // Two competitions.
      handle.sqlite
        .prepare(
          "INSERT INTO competitions (id, name, date, receipt_template, auto_print, created_at_ms) VALUES ('c1', 't', '2026-05-14', 'classic', 0, 1)"
        )
        .run();
      handle.sqlite
        .prepare(
          "INSERT INTO competitions (id, name, date, receipt_template, auto_print, created_at_ms) VALUES ('c2', 't', '2026-05-15', 'classic', 0, 1)"
        )
        .run();
      handle.sqlite
        .prepare(
          'INSERT INTO hired_cards (competition_id, card_number, marked_at_ms) VALUES (?, ?, ?)'
        )
        .run('c1', 12345, 1);
      handle.sqlite
        .prepare(
          'INSERT INTO hired_cards (competition_id, card_number, marked_at_ms) VALUES (?, ?, ?)'
        )
        .run('c2', 12345, 2);
      const rows = handle.sqlite
        .prepare<
          unknown[],
          { competition_id: string }
        >('SELECT competition_id FROM hired_cards ORDER BY competition_id')
        .all();
      assert.equal(rows.length, 2);
    } finally {
      handle.close();
    }
  });

  test('same card_number under same competition_id is rejected', () => {
    const handle = openDatabase(':memory:');
    try {
      handle.sqlite
        .prepare(
          "INSERT INTO competitions (id, name, date, receipt_template, auto_print, created_at_ms) VALUES ('c1', 't', '2026-05-14', 'classic', 0, 1)"
        )
        .run();
      handle.sqlite
        .prepare(
          'INSERT INTO hired_cards (competition_id, card_number, marked_at_ms) VALUES (?, ?, ?)'
        )
        .run('c1', 99999, 1);
      assert.throws(() => {
        handle.sqlite
          .prepare(
            'INSERT INTO hired_cards (competition_id, card_number, marked_at_ms) VALUES (?, ?, ?)'
          )
          .run('c1', 99999, 2);
      }, /UNIQUE constraint|PRIMARY KEY/);
    } finally {
      handle.close();
    }
  });
});

